import { existsSync, readFileSync } from 'fs'
import { join } from 'path'

const AI_BOT_COMMENT = '# AI crawler rules — managed by react-ai-helmet'
const AI_BOT_END     = '# end react-ai-helmet'

/**
 * All known AI crawler user-agent strings (as of 2025).
 * Keeping these in one place makes them easy to update.
 */
export const KNOWN_AI_BOTS = [
  'GPTBot',           // OpenAI
  'ChatGPT-User',     // OpenAI browsing
  'ClaudeBot',        // Anthropic
  'Claude-Web',       // Anthropic web
  'Google-Extended',  // Google Bard/Gemini training
  'PerplexityBot',    // Perplexity
  'cohere-ai',        // Cohere
  'anthropic-ai',     // Anthropic API
  'CCBot',            // Common Crawl (used by many LLMs)
  'omgili',           // Various AI training
  'Diffbot',          // AI data extraction
  'YouBot',           // You.com
]

/**
 * Build or update robots.txt content with AI bot rules.
 *
 * Strategy:
 * 1. If robots.txt exists, preserve all existing rules.
 * 2. Remove any previous react-ai-helmet managed block.
 * 3. Append the new AI block at the end.
 */
export function buildRobotsTxt(config, outDir) {
  const existingPath = join(outDir, 'robots.txt')
  let existing = ''

  if (existsSync(existingPath)) {
    existing = readFileSync(existingPath, 'utf8')
    // Strip previous managed block
    existing = stripManagedBlock(existing)
  }

  const { disallowPaths } = config.robots
  const explicitDeny = config.robots.denyBots || []

  // An explicit deny wins over an allow; denied bots that aren't in the known
  // list still get a Disallow block.
  const allowBots = config.robots.allowBots.filter((bot) => !explicitDeny.includes(bot))
  const denyBots = [
    ...new Set([
      ...KNOWN_AI_BOTS.filter((bot) => !allowBots.includes(bot)),
      ...explicitDeny,
    ]),
  ]

  const lines = []

  // Preserve existing content
  if (existing.trim()) {
    lines.push(existing.trimEnd())
    lines.push('')
  }

  // Managed block header
  lines.push(AI_BOT_COMMENT)
  lines.push(`# Generated by react-ai-helmet on ${new Date().toISOString().split('T')[0]}`)
  lines.push('')

  // Allow rules for explicitly allowed bots
  for (const bot of allowBots) {
    lines.push(`User-agent: ${bot}`)
    for (const path of disallowPaths) {
      lines.push(`Disallow: ${path}`)
    }
    lines.push('Allow: /')
    lines.push('')
  }

  // Deny rules for all other known AI bots
  if (denyBots.length > 0) {
    for (const bot of denyBots) {
      lines.push(`User-agent: ${bot}`)
      lines.push('Disallow: /')
      lines.push('')
    }
  }

  // Sitemap directive (top-level) so crawlers can discover the sitemap.
  if (config.sitemap?.generate && config.siteUrl) {
    const base = config.siteUrl.replace(/\/$/, '')
    lines.push(`Sitemap: ${base}/sitemap.xml`)
    lines.push('')
  }

  lines.push(AI_BOT_END)

  return lines.join('\n')
}

function stripManagedBlock(content) {
  const start = content.indexOf(AI_BOT_COMMENT)
  const end   = content.indexOf(AI_BOT_END)

  if (start === -1) return content

  const before = content.slice(0, start).trimEnd()
  const after  = end !== -1
    ? content.slice(end + AI_BOT_END.length)
    : ''

  return (before + '\n' + after.trimStart()).trimEnd()
}
